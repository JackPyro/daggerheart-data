function getModuleCompendiums() {
  const moduleName = "daggerheart-data";
  // Ensure the module is enabled
  if (!game.modules.get(moduleName).active) {
    console.error(`Module ${moduleName} is not active.`);
    return;
  }

  // Get the compendiums associated with the module
  const compendiums = game.packs
    .filter((pack) => pack.metadata.packageName === moduleName)
    .reduce((acc, comp) => {
      acc[comp.metadata.name] = comp;
      return acc;
    }, {});

  return compendiums;
}

